        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets RGB color. </summary>
        ///
        /// <remarks>   Dtorres, 20160815. </remarks>
        ///
        /// <param name="brushName">  Ex looks like "rgb(255,255,255)". </param>
        ///
        /// <returns>   The RGB color. </returns>
        ///-------------------------------------------------------------------------------------------------

        private static XSolidBrush GetRGBColor(string rgbString)
        {
            string rgbStringPrepped = rgbString.Remove(rgbString.Length - 1, 1);
            rgbStringPrepped = rgbStringPrepped.Substring(4);
            string[] rgbValues = rgbStringPrepped.Split(new char[] { ',' });
            if (rgbValues.Length != 3)
                throw new Exception($"Invalid RGB string {rgbString}");

            int red = Convert.ToInt32(rgbValues[0].ToString());
            int green = Convert.ToInt32(rgbValues[1].ToString());
            int blue = Convert.ToInt32(rgbValues[2].ToString());

            var brush = new XSolidBrush(XColor.FromArgb(red, green, blue));
            return brush;
        }
